Map::Map(GameManager* game) {
	this->game = game;

	floor = TextureManager::LoadTexture("Art\World\Caves\Backgrounds\tile_mines");
	wallTop = TextureManager::LoadTexture("Art\World\Caves\Backgrounds\wall_tile_top");
	wallBott = TextureManager::LoadTexture("Art\World\Caves\Backgrounds\wall_tile_bottom");

	LoadMap(test);

	source.x = 0;
	source.y = 0;
	dest.x = 0;
	dest.y = 0;

	source.h = 320;
	source.w = 320;
	dest.h = 320;
	dest.w = 320;
}

void Map::LoadMap(int arr[18][31]) {

	for (int row = 0; row < 18; row++) {
		for (int col = 0; col < 31; col++) {
			map[row][col] = arr[row][col];
		}
	}
}

void Map::DrawMap() {

	int tileType = 0;

	for (int row = 0; row < 18; row++) {
		for (int col = 0; col < 31; col++) {
			tileType = map[row][col];

			dest.x = col * 320;
			dest.y = row * 320;

			switch (tileType) {
			case 0:
				TextureManager::Draw(game->getRenderer(), floor, source, dest);
				break;
			case 1:
				TextureManager::Draw(game->getRenderer(), wallTop, source, dest);
				break;
			case 2:
				TextureManager::Draw(game->getRenderer(), wallBott, source, dest);
				break;
			default:
				break;
			}
		}
	}

}